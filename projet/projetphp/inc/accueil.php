                    <h3>L’AFPAR, un acteur essentiel de la formation professionnelle au service de l'Homme, de l'Economie et du Territoire</h3>

                    <p>
                        Créée en 1963, l’AFPAR a fêté ses 50 ans en 2013.
                        Acteur majeur et incontournable à la Réunion, la SPL AFPAR, Assistance à la Formation Professionnelle des Adultes à la Réunion, est un organisme de formation dédié aux adultes
                    </p>

                    <p>
                        L’AFPAR apporte sa contribution aux politiques publiques menées en faveur des réunionnais et conduit une mission d’intérêt général.
                    </p>

                    <h3>Une formation, un diplôme</h3>

                    <p>
                        Les formations AFPAR conduisent à un Titre Professionnel, reconnu par les entreprises et délivré par le Ministère chargé de l’Emploi. Ces titres peuvent être de niveau :
                    </p>


                    <ul>

                        <li>V (CAP et équivalent)</li>
                        <li>IV (Bac et équivalent)</li>
                        <li>III (BTS et équivalent) </li>

                    </ul>

                    <p>
                        <b><i>"L’AFPAR, des formations rémunérées pour les demandeurs d’emploi"</i></b>
                    </p>

